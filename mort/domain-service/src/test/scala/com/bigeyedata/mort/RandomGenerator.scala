package com.bigeyedata.mort

trait RandomGenerator {
  def randomString(length: Int = 3) = {
    val r = new scala.util.Random
    val sb = new StringBuilder
    for (i <- 1 to length) {
      sb.append(r.nextPrintableChar)
    }
    sb.toString
  }
}
